import random

print(random.randint(5, 20))  # Smallest number = 5; Largest number = 20
print(random.randrange(3, 10, 2))  # Smallest number = 3; Largest number = 9
print(random.uniform(2.5, 5.5))  # Smallest number = 2.5 ; Largest number = 5.5
print(random.randint(1, 100))
